function applyDocumentLanguage(lang) {
  if (typeof document !== 'undefined') {
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en'
  }
}

export function setLanguage(nextLanguage) {
  language.value = nextLanguage === 'en' ? 'en' : 'zh'
  localStorage.setItem(STORAGE_KEY, language.value)
  applyDocumentLanguage(language.value)
}

export function t(key, params = {}) {
  const template = messages[language.value]?.[key] ?? messages.zh[key] ?? key
  return template.replace(/\{(\w+)\}/g, (_, name) => params[name] ?? '')
}

export function strategyLabel(strategy) {
  if (!strategy) return '-'
  return t(`strategy.${strategy}`)
}

export function apiKeyStatusLabel(status) {
  if (!status) return '-'
  return t(`apiKeys.status.${status}`)
}

